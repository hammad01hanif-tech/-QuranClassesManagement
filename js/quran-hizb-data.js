// Helper function to get Hizb by number
export function getHizbByNumber(hizbNumber) {
  return quranHizbData.find(hizb => hizb.number === hizbNumber) || null;
}

// Helper function to get Hizb details as formatted string
export function getHizbDescription(hizbNumber) {
  const hizb = getHizbByNumber(hizbNumber);
  if (!hizb) return "حزب غير موجود";
  
  let description = `الحزب ${hizb.number}: ${hizb.name}\n`;
  
  hizb.surahs.forEach(surah => {
    if (surah.verses === "كاملة") {
      description += `• سورة ${surah.name} (كاملة)\n`;
    } else {
      description += `• سورة ${surah.name} (${surah.verses})\n`;
    }
  });
  
  if (hizb.pages > 0) {
    description += `\nعدد الصفحات: ${hizb.pages} صفحة`;
  }
  
  return description;
}

// Helper function to format Hizb for display
export function formatHizbForDisplay(hizbNumber) {
  const hizb = getHizbByNumber(hizbNumber);
  if (!hizb) return null;
  
  // Format surahs list
  const surahsList = hizb.surahs.map(surah => {
    if (surah.verses === "كاملة") {
      return `${surah.name}`;
    } else {
      return `${surah.name} (${surah.verses})`;
    }
  }).join(' - ');
  
  return {
    number: hizb.number,
    name: hizb.name,
    surahsList: surahsList,
    pages: hizb.pages,
    description: hizb.description
  };
}

// Get total number of Ahzab
export function getTotalAhzab() {
  return quranHizbData.length;
}

// Check if Hizb number is valid
export function isValidHizbNumber(hizbNumber) {
  return hizbNumber >= 1 && hizbNumber <= getTotalAhzab();
}
